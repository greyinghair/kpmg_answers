## Shorthand Arithmetic
# to shorten code length

# e.g using arithmetic with variables to set new value of variable

number = 3 # set variable of 'number' yo 3
number += 5 # means value of number, i.e. 3 + 5 = 8.  Sets number variable to now be 8
    # long version of the above line is:
    #   number = number + 5

number -= 2 # based on the above line setting variable to 8 we now -2 and set that as variable value = 6
number *= 4 # 24
number /= 2 # 12
number **= 2 # 144


# So mathematical operator (+-*/**) goes after the variable name and before the equals
