### Modules are Reuseable code so you don't have to create everything yourself ###
# Modules have to be imported, 2 x different samples below (importing "random")

# Sample-1 imports whole module
import random # imports the whole module called "random"

# Sample-2 imports specific function from a module
from math import floor # imports floor function from the module named math

# Syntax is....
#   from <module> import <function>
