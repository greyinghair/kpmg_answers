## Import multiple functions from a module (in the case 'floor' & 'ceiling')
from math import floor, ceil

# Floor function ROUNDS DOWN always

print(floor(4.8)) # 4

number = floor(3.2) # 3
print(number) # 3 (as above)

# ceil ("ceiling") as named, ROUNDS UP

print (ceil(9.99)) # 10

number2 = ceil(7.1) # 8
print(number2) # 8 (as above)

