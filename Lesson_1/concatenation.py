# Joining of multiple strings together
# You can only add string to another string, not to a float nor an integer

# No spaces in output
first_name = "Dan"
last_name = "Stacey"
full_name = first_name + last_name
print("Hello" + full_name)

# Include a space in fullname variable and space in the print after Good morning
first_name = "Dan"
last_name = "Stacey"
full_name = first_name + " " + last_name
print("Good morning, " + full_name + "!")